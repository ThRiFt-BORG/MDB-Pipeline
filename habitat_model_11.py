def fig_score_trends(trend: pd.DataFrame, panel: pd.DataFrame):
    print("  Generating fig_habitat_score_trends.png …")

    regions = [r for r in REGION_COLOURS if r in trend["region"].unique()]
    years   = sorted(trend["year"].unique().astype(int))

    fig, (ax_line, ax_box) = plt.subplots(
        1, 2, figsize=(18, 8), facecolor=LIGHT_GREY,
        gridspec_kw={"wspace": 0.30}
    )

    # --- Line: mean score per region per year ---
    for region in regions:
        sub   = trend[trend["region"] == region].sort_values("year")
        color = REGION_COLOURS[region]
        label = region if len(region) < 30 else region[:28] + "…"
        ax_line.plot(sub["year"], sub["mean_score"],
                     color=color, linewidth=2, marker="o",
                     markersize=4, zorder=3, label=label)
        ax_line.fill_between(
            sub["year"],
            sub["mean_score"] - sub["std_score"],
            sub["mean_score"] + sub["std_score"],
            color=color, alpha=0.10, zorder=2
        )

    style_axis(ax_line,
               xlabel="Year",
               ylabel="Mean habitat score (normalised 0–1)",
               title="Habitat suitability score trends by sub-basin  ·  2016–2025")
    ax_line.set_xticks(years)
    ax_line.set_xticklabels(years, rotation=35, ha="right")
    ax_line.set_ylim(0, 1)
    ax_line.legend(loc="upper left", fontsize=8, framealpha=0.96,
                   edgecolor=SPINE_COL)
    add_panel_label(ax_line, "a")

    # --- Box: score distribution per year (all clusters) ---
    year_data = [panel[panel["year"] == yr]["habitat_score"].dropna().values
                 for yr in years]
    bp = ax_box.boxplot(year_data, positions=years, widths=0.55,
                        patch_artist=True, notch=False,
                        medianprops=dict(color=DARK_GREY, linewidth=1.5),
                        whiskerprops=dict(color=MID_GREY, linewidth=0.8),
                        capprops=dict(color=MID_GREY, linewidth=0.8),
                        flierprops=dict(marker=".", color=MID_GREY,
                                        alpha=0.3, markersize=3))
    for patch in bp["boxes"]:
        patch.set_facecolor("#1F78B4")
        patch.set_alpha(0.35)
        patch.set_edgecolor(SPINE_COL)

    style_axis(ax_box,
               xlabel="Year",
               ylabel="Habitat score distribution (all clusters)",
               title="Annual habitat score distribution  ·  all MDB clusters")
    ax_box.set_xticks(years)
    ax_box.set_xticklabels(years, rotation=35, ha="right")
    ax_box.set_ylim(0, 1)
    add_panel_label(ax_box, "b")

    out = OUTPUTS / "fig_habitat_score_trends.png"
    fig.savefig(out, facecolor=LIGHT_GREY)
    plt.close(fig)
    print(f"    Saved → {out}")


