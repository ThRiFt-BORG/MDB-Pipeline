# Step 2 — Feature engineering + habitat score normalisation
# ---------------------------------------------------------------------------

def engineer_features(panel: pd.DataFrame) -> pd.DataFrame:
    print("  Engineering features …")

    # Recalculate raw score (NDWI dominant — wetland signal)
    panel["habitat_score_raw"] = panel["NDWI"] * 0.6 + panel["NDVI"] * 0.4

    # Min-max normalise per year so scores are comparable across years
    panel["habitat_score"] = panel.groupby("year")["habitat_score_raw"] \
        .transform(lambda x: (x - x.min()) / (x.max() - x.min() + 1e-9))

    # Wetland index: positive NDWI = surface water / wetland
    panel["is_wetland"] = (panel["NDWI"] > 0).astype(int)

    # Vegetation vigour: NDVI z-score within year
    panel["ndvi_zscore"] = panel.groupby("year")["NDVI"] \
        .transform(lambda x: (x - x.mean()) / (x.std() + 1e-9))

    # Interannual stability: std of habitat_score across years per cluster
    stability = panel.groupby("cluster_id")["habitat_score"] \
        .std().reset_index().rename(columns={"habitat_score": "score_stability"})
    panel = panel.merge(stability, on="cluster_id", how="left")

    print(f"    habitat_score range: "
          f"{panel['habitat_score'].min():.3f} – {panel['habitat_score'].max():.3f}")
    print(f"    Wetland clusters (NDWI>0): "
          f"{panel[panel['is_wetland']==1]['cluster_id'].nunique()} "
          f"unique clusters")
    return panel


