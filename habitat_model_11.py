def load_and_merge(processed_dir: Path) -> tuple[pd.DataFrame, pd.DataFrame]:
    """
    Returns:
      panel  — long-format: one row per cluster per year (2620 rows)
      static — one row per cluster (262 rows) with static attributes
    """
    print("  Loading Sentinel-2 annual panels …")
    s2_dfs = []
    for fname in S2_FILES:
        fpath = processed_dir / fname
        if not fpath.exists():
            sys.exit(f"ERROR: {fpath} not found. "
                     f"Place all GEE CSVs in {processed_dir}")
        s2_dfs.append(pd.read_csv(fpath))
    panel = pd.concat(s2_dfs, ignore_index=True)
    print(f"    Combined panel: {panel.shape[0]:,} rows × {panel.shape[1]} cols")

    # Load clusters_summary.csv — authoritative source for static attributes
    summary_paths = [
        processed_dir / "clusters_summary.csv",
        ROOT / "outputs" / "clusters_summary.csv",
    ]
    summary_path = next((p for p in summary_paths if p.exists()), None)
    if summary_path is None:
        sys.exit("ERROR: clusters_summary.csv not found. "
                 "Run habitat_cluster_09.py first.")
    summary = pd.read_csv(summary_path)
    print(f"    Cluster summary: {len(summary)} clusters")

    # Recover columns that GEE dropped (multi_source, has_breeding, etc.)
    recover_cols = ["cluster_id", "multi_source", "has_breeding",
                    "habitat_tier", "total_abundance", "sources", "n_sources"]
    recover_cols = [c for c in recover_cols if c in summary.columns]
    panel = panel.drop(
        columns=[c for c in recover_cols if c != "cluster_id"
                 and c in panel.columns],
        errors="ignore"
    )
    panel = panel.merge(summary[recover_cols], on="cluster_id", how="left")

    # Static view — mean across all years per cluster
    static_cols = ["cluster_id", "region", "centroid_lat", "centroid_lon",
                   "multi_source", "has_breeding", "habitat_tier",
                   "total_abundance", "n_points", "n_species", "n_years",
                   "extent_km", "urban_fraction_pct", "n_sources", "sources"]
    static_cols = [c for c in static_cols if c in panel.columns]
    static = panel[static_cols].drop_duplicates(subset=["cluster_id"])

    print(f"    Null check — multi_source: {panel['multi_source'].isna().sum()}, "
          f"has_breeding: {panel['has_breeding'].isna().sum()}")
    return panel, static


