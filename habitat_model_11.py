def main():
    parser = argparse.ArgumentParser(
        description="MDB habitat suitability model — Phase 3")
    parser.add_argument("--processed",  type=str, default=None,
                        help="Path to processed/ folder with GEE CSVs")
    parser.add_argument("--shapefile",  type=str, default=None)
    args = parser.parse_args()

    processed_dir = (Path(args.processed) if args.processed else PROCESSED)
    shapefile = (Path(args.shapefile) if args.shapefile
                 else next((p for p in DEFAULT_SHAPEFILES if p.exists()), None))

    if shapefile and shapefile.exists():
        print(f"  Shapefile: {shapefile}")
    else:
        print("  Shapefile not found — bbox fallback.")
        shapefile = None

    print("\n" + "=" * 60)
    print("  MDB WATERBIRD PIPELINE — habitat_model_11.py")
    print("=" * 60)

    # Steps
    panel, static    = load_and_merge(processed_dir)
    panel            = engineer_features(panel)
    rf, cluster_mean, importance_df, cvr2 = train_model(panel)
    static, trend    = build_final(panel, static, cluster_mean)

    print("\n  Generating figures …")
    fig_suitability_map(static, shapefile)
    fig_score_trends(trend, panel)
    fig_feature_importance(importance_df, cvr2)

    print("\n  Exporting CSVs …")
    export_final(static, panel)

    print("\n" + "=" * 60)
    print("  Outputs written to: outputs/")
    print("  fig_habitat_suitability_map.png")
    print("  fig_habitat_score_trends.png")
    print("  fig_feature_importance.png")
    print("  habitat_suitability_final.csv")
    print("  habitat_scores_annual.csv")
    print("=" * 60 + "\n")


if __name__ == "__main__":
    main()