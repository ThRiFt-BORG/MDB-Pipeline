# Step 3 — Random Forest suitability model
# ---------------------------------------------------------------------------

FEATURES = ["NDVI", "NDWI", "EVI",
            "extent_km", "ndvi_zscore", "score_stability"]

def train_model(panel: pd.DataFrame) -> tuple:
    print("  Training Random Forest habitat suitability model …")

    # Use mean across years per cluster for a stable static model
    cluster_mean = panel.groupby("cluster_id")[FEATURES + ["total_abundance"]] \
        .mean().reset_index()
    cluster_mean = cluster_mean.dropna(subset=FEATURES + ["total_abundance"])

    X = cluster_mean[FEATURES].to_numpy(dtype=float)
    y = np.log1p(
        (cluster_mean["total_abundance"] / cluster_mean["extent_km"].clip(lower=0.1))
        .to_numpy(dtype=float)
    )
    rf = RandomForestRegressor(
        n_estimators=300,
        max_depth=8,
        min_samples_leaf=3,
        random_state=42,
        n_jobs=-1
    )
    rf.fit(X, y)

    # Cross-validated R²
    cv   = KFold(n_splits=5, shuffle=True, random_state=42)
    cvr2 = cross_val_score(rf, X, y, cv=cv, scoring="r2")
    print(f"    CV R² = {cvr2.mean():.3f} ± {cvr2.std():.3f}")

    # Predict suitability for all clusters (mean features)
    raw_pred = rf.predict(X)
    pmin, pmax = raw_pred.min(), raw_pred.max()
    cluster_mean["predicted_suitability"] = (raw_pred - pmin) / (pmax - pmin + 1e-9)

    # Permutation importance for interpretability
    perm_result = permutation_importance(rf, X, y, n_repeats=20, random_state=42)
    importance_df = pd.DataFrame({
        "feature":    FEATURES,
        "importance": np.array(perm_result["importances_mean"]),
        "std":        np.array(perm_result["importances_std"]),
    }).sort_values("importance", ascending=False)

    print(f"\n    Feature importances:")
    for _, row in importance_df.iterrows():
        print(f"      {row['feature']:20s}  {row['importance']:.4f} ± {row['std']:.4f}")

    return rf, cluster_mean, importance_df, cvr2


# ---------------------------------------------------------------------------
# Step 4 — Merge predictions back to static + panel
# ---------------------------------------------------------------------------

def build_final(panel: pd.DataFrame, static: pd.DataFrame,
                cluster_mean: pd.DataFrame) -> tuple[pd.DataFrame, pd.DataFrame]:

    pred_cols = ["cluster_id", "predicted_suitability"]
    static = static.merge(cluster_mean[pred_cols], on="cluster_id", how="left")

    # Suitability tier
    q33 = static["predicted_suitability"].quantile(0.33)
    q66 = static["predicted_suitability"].quantile(0.66)
    static["suitability_tier"] = pd.cut(
        static["predicted_suitability"],
        bins=[-np.inf, q33, q66, np.inf],
        labels=["Low", "Moderate", "High"]
    )

    # Annual trends — mean habitat_score per year × region
    trend = panel.groupby(["year", "region"])["habitat_score"] \
        .agg(["mean", "std"]).reset_index()
    trend.columns = ["year", "region", "mean_score", "std_score"]

    print(f"\n  High suitability clusters : "
          f"{(static['suitability_tier']=='High').sum()}")
    print(f"  Moderate suitability      : "
          f"{(static['suitability_tier']=='Moderate').sum()}")
    print(f"  Low suitability           : "
          f"{(static['suitability_tier']=='Low').sum()}")

    return static, trend


