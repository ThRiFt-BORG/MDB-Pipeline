def fig_suitability_map(static: pd.DataFrame, shapefile):
    print("  Generating fig_habitat_suitability_map.png …")

    fig, (ax_map, ax_rank) = plt.subplots(
        1, 2, figsize=(18, 9), facecolor=LIGHT_GREY,
        gridspec_kw={"wspace": 0.25, "width_ratios": [2, 1]}
    )

    # --- Map ---
    ax_map.set_facecolor(WATER_COL)
    bounds = draw_mdb_boundary(ax_map, shapefile)
    pad = 0.35
    ax_map.set_xlim(bounds[0] - pad, bounds[2] + pad)
    ax_map.set_ylim(bounds[1] - pad, bounds[3] + pad)

    sc = ax_map.scatter(
        static["centroid_lon"], static["centroid_lat"],
        c=static["predicted_suitability"],
        cmap=SUIT_CMAP, vmin=0, vmax=1,
        s=static["extent_km"].clip(2, 100) * 1.5 + 8,
        alpha=0.85, zorder=4,
        edgecolors="white", linewidths=0.3, rasterized=True
    )

    # Star breeding confirmed sites
    breed = static[static["has_breeding"] == True]
    ax_map.scatter(breed["centroid_lon"], breed["centroid_lat"],
                   s=80, marker="*", color="#FFFFFF", zorder=6,
                   edgecolors=DARK_GREY, linewidths=0.5,
                   label=f"Breeding confirmed ({len(breed)})")

    cbar = fig.colorbar(sc, ax=ax_map, fraction=0.025, pad=0.02)
    cbar.set_label("Predicted habitat suitability (0–1)",
                   fontsize=9, color=DARK_GREY)
    cbar.ax.tick_params(labelsize=8)

    ax_map.set_aspect("equal")
    ax_map.set_xlabel("Longitude (°E)", fontsize=9, color=DARK_GREY, labelpad=4)
    ax_map.set_ylabel("Latitude (°N)",  fontsize=9, color=DARK_GREY, labelpad=4)
    ax_map.tick_params(labelsize=8.5, color=SPINE_COL, direction="out")
    ax_map.grid(visible=True, color="#D4E7F0", linestyle="-",
                linewidth=0.28, alpha=0.55, zorder=0)
    for sp in ax_map.spines.values():
        sp.set_visible(True)
        sp.set_edgecolor(SPINE_COL)
        sp.set_linewidth(0.65)

    ax_map.set_title(
        "Predicted waterbird habitat suitability  ·  Murray–Darling Basin\n"
        "Dot size ∝ cluster extent (km).  ★ = breeding confirmed.",
        fontweight="bold", color=DARK_GREY, pad=7
    )
    leg = ax_map.legend(loc="upper left", fontsize=8.5,
                        framealpha=0.96, edgecolor=SPINE_COL)
    leg.get_frame().set_linewidth(0.5)
    add_panel_label(ax_map, "a", x=-0.04)

